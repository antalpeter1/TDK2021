def run_sequence(cf, duration):
    commander = cf.high_level_commander
    cf.param.set_value('stabilizer.controller', '2')
    commander.takeoff(0.1, 2.0)
    time.sleep(5.0)
    cf.param.set_value('stabilizer.controller', '1')
    time.sleep(5.0)
    commander.land(0.0, 2.0)
    time.sleep(2)
    commander.stop()


