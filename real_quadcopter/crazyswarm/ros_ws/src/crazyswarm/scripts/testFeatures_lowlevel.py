    with SyncCrazyflie(uri, cf=Crazyflie(rw_cache='./cache')) as scf:
        cf = scf.cf
        activate_high_level_commander(cf)
        #reset_estimator(cf)
        duration = 2
        run_sequence(cf, duration)
